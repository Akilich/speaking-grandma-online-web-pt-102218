string.new = "WHAT DID YOU EAT TODAY?"
def speak_to_grandma(string)
if (string == string.upcase)
  return "I LOVE YOU TOO PUMPKIN!"
else (string.new == string.new.downcase)
  return "HUH?! SPEAK UP, SONNY!"
if (string == string.upcase)
  return "NO, NOT SINCE 1938!"
else
  return "NO, NOT SINCE 1938!"
end 
end
